<?php

namespace Tests\Feature\Bot\TreasureHunt;

use Illuminate\Database\Schema\Blueprint;
use Illuminate\Foundation\Testing\RefreshDatabase;
use Illuminate\Foundation\Testing\WithFaker;
use Illuminate\Support\Facades\DB;
use Illuminate\Support\Facades\Schema;
use Tests\TestCase;

class ComparePirepTest extends TestCase
{
    use RefreshDatabase;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub
        Schema::connection('sqlite')->create('th_participants', function (Blueprint $table) {
            $table->string('pilot')->primary();
            $table->integer('progress')->default(1);
            $table->boolean('is_completed')->default(false);
            $table->timestamps();
        });

        Schema::connection('sqlite')->create('th_clues', function (Blueprint $table) {
            $table->increments('id');
            $table->integer('clue_order');
            $table->text('clue');
            $table->string('answer');
        });

        DB::table('th_clues')->insert([
            ['clue_order' => 1, 'clue' => 'First Clue', 'answer' => 'AYMR'],
            ['clue_order' => 2, 'clue' => 'Second Clue', 'answer' => 'AYMH'],
        ]);
    }
    /**
     * A basic feature test example.
     *
     * @return void
     */
    public function test_successful_answer_returns_next_clue()
    {
        DB::table('th_participants')->insert([
            'pilot' => 'BDV0001'
        ]);

        $data = ['participantId' => 'BDV0001', 'icao' => 'AYMR'];
        $headers = ['X-TOKEN' => env('BOT_TOKEN')];
        $response = $this->postJson('/api/bot/treasure/result', $data, $headers);
        $response->assertStatus(200);
    }

    public function test_successful_answer_returns_no_more_clues()
    {
        DB::table('th_participants')->insert([
            'pilot' => 'BDV0001',
            'progress' => 2
        ]);

        $data = ['participantId' => 'BDV0001', 'icao' => 'AYMH'];
        $headers = ['X-TOKEN' => env('BOT_TOKEN')];
        $response = $this->postJson('/api/bot/treasure/result', $data, $headers);
        $response->assertStatus(404);
        $response->assertJson([
            'message' => 'no more clues'
        ]);
        $this->assertDatabaseHas('th_participants', [
            'pilot' => 'BDV0001',
            'is_completed' => true
        ]);
    }

    public function test_successful_answer_updates_participant_progress()
    {
        DB::table('th_participants')->insert([
            'pilot' => 'BDV0001'
        ]);

        $data = ['participantId' => 'BDV0001', 'icao' => 'AYMR'];
        $headers = ['X-TOKEN' => env('BOT_TOKEN')];
        $response = $this->postJson('/api/bot/treasure/result', $data, $headers);
        $this->assertDatabaseHas('th_participants', [
            'pilot' => 'BDV0001',
            'progress' => 2
        ]);
    }

    public function test_incorrect_answer_returns_unprocessable()
    {
        DB::table('th_participants')->insert([
            'pilot' => 'BDV0001'
        ]);

        $data = ['participantId' => 'BDV0001', 'icao' => 'EGBB'];
        $headers = ['X-TOKEN' => env('BOT_TOKEN')];
        $response = $this->postJson('/api/bot/treasure/result', $data, $headers);
        $response->assertStatus(422);
    }
}
